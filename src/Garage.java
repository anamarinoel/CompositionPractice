public class Garage {
    private String place;
    private int two;
    private int tree;

    public Garage(String place, int two, int tree) {
        this.place = place;
        this.two = two;
        this.tree = tree;
    }

    public String getPlace() {
        return place;
    }

    public int getTwo() {
        return two;
    }

    public int getTree() {
        return tree;
    }
}
